            <br><br>Gesture recognition could help people that are physically impaired by eliminating joysticks, mice, and keyboards. People that have issues using these technologies could replace them with gestures that can be adjusted to their liking. This could make interaction with technology easier to use and understand, and getting rid of joysticks, mice, and keyboards could make the use of technology more enjoyable for people who have a difficult time manipulating those devices.</p>

