            <p class="indented">A gesture is a movement of the body with the focus on the hands and the head to convey a message. A gesture can be used in passing information to a technical system, such as tablets, computers, and cell phones. In this case, gesture recognition technology is necessary to ensure that the right information is getting passed into the system.
            <br><br>Gesture recognition technology is manipulated by the user using body movements to interact with their technology (tv, phone, computer, etc...) to send a command for the technology to read and act on. An example of gesture recognition is the use of a camera on a laptop and the user waving their hand in front of the camera to “wake up” the program. Another gesture that is well known is pinching one’s fingers close together and pushing apart on the screen/mouse track to zoom in and out of the view.
            <br><br>People use gestures as an input for their computers for a variety of reasons. There could be accessible reasons, sense of ease, enhancing  a game experience, and/or more. When it comes to accessible reasons it could help the blind, deaf, and physically impaired.
